    private void btInserirfornActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirfornActionPerformed
         DefaultTableModel tbProduto = (DefaultTableModel) tbFornecedores.getModel();
        Object [] dados = {txtCodforn.getText(), txtItemforn.getText(), txtQntdforn.getText(), txtFornecedorforn.getText()};
        tbProduto.addRow(dados);
    }//GEN-LAST:event_btInserirfornActionPerformed

    private void btExcluirfornActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirfornActionPerformed
          if (tbFornecedores.getSelectedRow() != -1){
        DefaultTableModel tbProduto = (DefaultTableModel) tbFornecedores.getModel();
        tbProduto.removeRow(tbFornecedores.getSelectedRow());
         }
    }//GEN-LAST:event_btExcluirfornActionPerformed

    private void btAlterarfornActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarfornActionPerformed
        if(tbFornecedores.getSelectedRow() != -1){
            tbFornecedores.setValueAt(txtCodforn.getText(), tbFornecedores.getSelectedRow(), 0);
            tbFornecedores.setValueAt(txtItemforn.getText(), tbFornecedores.getSelectedRow(), 1);
            tbFornecedores.setValueAt(txtQntdforn.getText(), tbFornecedores.getSelectedRow(), 2);
            tbFornecedores.setValueAt(txtFornecedorforn.getText(), tbFornecedores.getSelectedRow(), 3);
         }      
    }//GEN-LAST:event_btAlterarfornActionPerformed

